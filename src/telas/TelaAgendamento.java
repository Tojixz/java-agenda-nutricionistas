    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        cbxnutricionista.addItem("Selecione o nutricionista");
        for(int x=0; x<i; x++){
            cbxnutricionista.addItem(lista[x].getnome());
        }
        cbxpaciente.addItem("Selecione o paciente");
        for(int z=0; z<indicep; z++){
            cbxpaciente.addItem(vpessoa[z].getnome());
        }
    }//GEN-LAST:event_formWindowOpened

