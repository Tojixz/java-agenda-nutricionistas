    private void txthorarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txthorarioActionPerformed
        // TODO add your handling code here:
    }//GEN-LAST:event_txthorarioActionPerformed
    
    int[] mes = {0, 1, 4, 5, 1, 3, 6, 1, 4, 0, 2, 5, 0};
    
    Nutricionista[] lista = new Nutricionista[5];
    int i=0;
    Pessoa[] vpessoa = new Pessoa[10];
    int indicep=0;
    
    public void recebe(Pessoa[] vp, int ip, Nutricionista[]vn, int in){
        vpessoa=vp;
        indicep=ip;
        lista=vn;
        i=in;
    }
    
    private void cbxmesItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbxmesItemStateChanged
        int m = Integer.parseInt(String.valueOf(cbxmes.getSelectedIndex()));
        int d=0, df;
        if(m==1 || m==3 || m==5 || m==7 || m==8 || m==10 || m==12){
            df=31;
        }else{
            if(m==2){df=29;}
            else{df=30;}
        }
        for(int lin=0; lin<6; lin++){
            for(int col=0; col<7; col++){
                jTable1.setValueAt(null, lin, col);
            }
        }
        for(int lin=0; lin<6; lin++){
            for(int col=0; col<7; col++){
                if(d==0) {
                    col=mes[m];
                    d=1;
                }
                if(d<=df){
                    jTable1.setValueAt(d, lin, col);
                    d++;
                } else {
                    break;
                }
            }
        }
    }//GEN-LAST:event_cbxmesItemStateChanged

